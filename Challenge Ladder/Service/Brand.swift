import Foundation
import UIKit

extension UIColor{
    
    public class var brandPurple: UIColor {
        return UIColor(red: 90/255, green: 30/255, blue: 125/255, alpha: 1.0)
    }
    
    public class var background: UIColor {
        return UIColor(red: 150/255, green: 205/255, blue: 225/255, alpha: 1.0)
    }
    
    public class var navBarColor: UIColor {
        return UIColor.clear
    }
    
    public class var charcoal: UIColor {
        return UIColor(red: 60/255, green: 60/255, blue: 60/255, alpha: 1.0)
    }
    
    public class var darkcharcoal: UIColor {
        return UIColor(red: 10/255, green: 10/255, blue: 10/255, alpha: 1.0)
    }
    
}
